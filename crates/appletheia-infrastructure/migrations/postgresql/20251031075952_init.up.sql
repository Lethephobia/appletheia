  aggregate_type   TEXT NOT NULL,
  owner_aggregate_id UUID NOT NULL,
  namespace        TEXT NOT NULL,
  normalized_value TEXT NOT NULL,

  UNIQUE (aggregate_type, namespace, normalized_value)
