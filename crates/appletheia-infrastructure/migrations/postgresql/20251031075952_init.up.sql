-- relationships (Aggregate × ReBAC)
CREATE TABLE IF NOT EXISTS relationships (
  id                     UUID        PRIMARY KEY,
  aggregate_type         TEXT        NOT NULL,
  aggregate_id           UUID        NOT NULL,
  relation               TEXT        NOT NULL,

  subject_aggregate_type TEXT        NOT NULL,
  subject_aggregate_id   UUID,
  subject_relation       TEXT,
  subject_is_wildcard    BOOLEAN     NOT NULL DEFAULT false,

  created_at             TIMESTAMPTZ NOT NULL DEFAULT now(),

  CONSTRAINT relationships_subject_check CHECK (
    (
      subject_is_wildcard = true
      AND subject_aggregate_id IS NULL
      AND subject_relation IS NULL
    )
    OR
    (
      subject_is_wildcard = false
      AND subject_aggregate_id IS NOT NULL
      AND subject_relation IS NULL
    )
    OR
    (
      subject_is_wildcard = false
      AND subject_aggregate_id IS NOT NULL
      AND subject_relation IS NOT NULL
    )
  )
);

CREATE INDEX IF NOT EXISTS idx_relationships_aggregate_relation
  ON relationships (aggregate_type, aggregate_id, relation);

CREATE UNIQUE INDEX IF NOT EXISTS idx_relationships_direct_uniq
  ON relationships (
    aggregate_type, aggregate_id, relation,
    subject_aggregate_type, subject_aggregate_id
  )
  WHERE subject_is_wildcard = false AND subject_relation IS NULL;

CREATE UNIQUE INDEX IF NOT EXISTS idx_relationships_subject_set_uniq
  ON relationships (
    aggregate_type, aggregate_id, relation,
    subject_aggregate_type, subject_aggregate_id, subject_relation
  )
  WHERE subject_is_wildcard = false AND subject_relation IS NOT NULL;

CREATE UNIQUE INDEX IF NOT EXISTS idx_relationships_wildcard_uniq
  ON relationships (
    aggregate_type, aggregate_id, relation,
    subject_aggregate_type
  )
  WHERE subject_is_wildcard = true;

CREATE INDEX IF NOT EXISTS idx_relationships_subject_direct
  ON relationships (
    subject_aggregate_type, subject_aggregate_id, relation,
    aggregate_type, aggregate_id
  )
  WHERE subject_is_wildcard = false;

CREATE INDEX IF NOT EXISTS idx_relationships_subject_wildcard
  ON relationships (
    subject_aggregate_type, relation,
    aggregate_type, aggregate_id
  )
  WHERE subject_is_wildcard = true;

-- auth token revocations
CREATE TABLE IF NOT EXISTS auth_token_revocations (
  id         UUID        PRIMARY KEY,
  token_id   UUID        NOT NULL UNIQUE,
  expires_at TIMESTAMPTZ NOT NULL,
  revoked_at TIMESTAMPTZ NOT NULL
);

CREATE INDEX IF NOT EXISTS idx_auth_token_revocations_expires_at
  ON auth_token_revocations (expires_at);

COMMENT ON TABLE auth_token_revocations IS 'Per-token revocations keyed by auth token id.';

CREATE TABLE IF NOT EXISTS auth_token_revocation_cutoffs (
  id                     UUID        PRIMARY KEY,
  subject_aggregate_type TEXT        NOT NULL,
  subject_aggregate_id   UUID        NOT NULL,
  revoke_before          TIMESTAMPTZ NOT NULL,
  updated_at             TIMESTAMPTZ NOT NULL,

  UNIQUE (subject_aggregate_type, subject_aggregate_id)
);

COMMENT ON TABLE auth_token_revocation_cutoffs IS 'Subject-wide revocation cutoffs for auth tokens.';

CREATE TABLE IF NOT EXISTS auth_token_exchange_codes (
  id                    UUID        PRIMARY KEY,
  code_hash             TEXT        NOT NULL UNIQUE,
  code_challenge_method TEXT,
  code_challenge        TEXT,
  encrypted_grant       BYTEA       NOT NULL,
  created_at            TIMESTAMPTZ NOT NULL,
  expires_at            TIMESTAMPTZ NOT NULL,
  consumed_at           TIMESTAMPTZ,
  CONSTRAINT auth_token_exchange_codes_protection_check CHECK (
    (
      code_challenge_method IS NULL
      AND code_challenge IS NULL
    )
    OR
    (
      code_challenge_method IS NOT NULL
      AND code_challenge IS NOT NULL
    )
  ),
  CONSTRAINT auth_token_exchange_codes_expires_check CHECK (expires_at >= created_at)
);

CREATE INDEX IF NOT EXISTS idx_auth_token_exchange_codes_expires_at
  ON auth_token_exchange_codes (expires_at);

COMMENT ON TABLE auth_token_exchange_codes IS 'Encrypted one-time exchange codes for issuing auth tokens.';
